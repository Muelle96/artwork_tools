<template>
    <Link :style="textStyle" class="flex font-semibold text-calendarText items-center px-8" :href="route('rooms.show', { room: room.id })">
        {{ room.name }}
    </Link>
</template>

<script setup>

import {Link, usePage} from "@inertiajs/vue3";
import {computed, ref} from "vue";

const zoom_factor = ref(usePage().props.user.zoom_factor ?? 1);

const props = defineProps({
    room: {
        type: Object,
        required: true
    }
})


const textStyle = computed(() => {
    const fontSize = `max(calc(${zoom_factor.value} * 0.875rem), 10px)`;
    const lineHeight = `max(calc(${zoom_factor.value} * 1.25rem), 1.3)`;
    return {
        fontSize,
        lineHeight,
    };
})
</script>

